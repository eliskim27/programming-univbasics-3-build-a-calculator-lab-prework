def addition(num1, num2)
  answer = num1 + num2
  puts answer
end

def subtraction(num1, num2)
  answer = num1 - num2
  puts answer
end

def division(num1, num2)
  answer = num1 / num2
  puts answer
end

def multiplication(num1, num2)
  answer = num1 * num2
  puts answer
end

def modulo(num1, num2)
  answer = num1 % num2
  puts answer
end

def square_root(num)
  answer = num.sqrt
  puts answer
end
